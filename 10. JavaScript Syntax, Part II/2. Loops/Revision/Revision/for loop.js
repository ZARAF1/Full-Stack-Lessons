// simple for loop
for (let count=0;count<10;count++){
    console.log(count)
}

// loop in reverse
for (let counter=10;counter>0;counter--){
    console.log(counter)
}

// iterating using for loops over arrays
let animals=['bear','cheetah','tiger'];
for(let i=0;i<animals.length;i++){
    console.log(animals[i])
}

// similar but with a variable

let manimals=['bear','cheetah','tiger'];
let manimalLength=manimals.length;
for(let i=0;i<manimalLength;i++){
    console.log(manimals[i])
}

