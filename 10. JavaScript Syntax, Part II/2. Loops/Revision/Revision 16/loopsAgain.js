// for loop


// example 1
for (let i = 0;i<5; i+=2) { // just checking if it works with mathematical assignment operators
    console.log(i)
}


// example 2
const vacationSpots = ["Manila","Pakistan","Jordan","France","Germany","Netherlands"];
for (let i=0;i<vacationSpots.length;i++){
    console.log(vacationSpots[i])
}

// reverse example 2
for (let j=vacationSpots.length-1;j>=0;j--){
    console.log(vacationSpots[j])
}

// example reverse for loop
for (let i=10;i>=5;i--){
    console.log(i)
}
