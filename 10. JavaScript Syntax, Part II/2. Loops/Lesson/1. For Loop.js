//// for loop on numbers
// example 1
for (let myNum = 5; myNum <= 10; myNum++) {
    console.log(myNum);
}

// example 2
for (let i = 0; i < 10; i++) {
    console.log(i)
}

//// for loop on string
let testString = "This is a string";
for (let i = 0; i < testString.length; i++) {
    console.log(testString[i])
}

// for loop on arrays
let arr = ["1a", "2b", "3c"];
for (let i = 0; i < arr.length; i++) {
    console.log(arr[i])
}